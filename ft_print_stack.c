#include"push_swap.h"

void	ft_print_stack(t_stack s)
{
	int	i;

	i = 0;
	while(i < s.n)
	{
		ft_putnbr_fd(s.t[i], 1);
		ft_putchar_fd('\n', 1);
		i++;
	}
	write(1, "-", 1);
	write(1, "\n", 1);
	write(1, &s.c, 1);
	write(1, "\n", 1);
}
